from tkinter import *
window=Tk()
window.title('Calculator')
window.geometry('350x350')
number=StringVar()
e1=Entry(window,textvariable=number)
e1.place(x=80,y=50,h=25,w=200)
def press(key):
    if key == "=":
        expression = e1.get()
        result = eval(expression)
        number.set(str(result))
    elif key == "C":
        number.set("")
    else:
        n1 = number.get()
        number.set(n1 + str(key))
